# ── Worker threads ────────────────────────────────────────────────────────────

class LoadWorker(QObject):
    finished = pyqtSignal(int)   # page count
    error    = pyqtSignal(str)

    def __init__(self, handler: PDFHandler, path: str):
        super().__init__()
        self._handler = handler
        self._path    = path

    def run(self):
        try:
            count = self._handler.open(self._path)
            self.finished.emit(count)
        except Exception as e:
            self.error.emit(str(e))


class ExportWorker(QObject):
    """Runs export_regions_as_pdf on a QThread."""
    finished = pyqtSignal(bool, str, int)
    error    = pyqtSignal(str)

    def __init__(self, handler: PDFHandler, regions: list, output_path: str):
        super().__init__()
        self._handler     = handler
        self._regions     = regions
        self._output_path = output_path

    def run(self):
        try:
            ok = self._handler.export_regions_as_pdf(
                self._regions, self._output_path)
            self.finished.emit(ok, self._output_path, len(self._regions))
        except Exception as e:
            self.error.emit(str(e))


class ResizeWorker(QObject):
    """Runs resize_document on a QThread."""
    finished = pyqtSignal(bool, str)
    error    = pyqtSignal(str)

    def __init__(self, handler: PDFHandler, width: float, height: float,
                 unit: str, output_path: str, out_format: str,
                 keep_aspect: bool):
        super().__init__()
        self._handler     = handler
        self._width       = width
        self._height      = height
        self._unit        = unit
        self._output_path = output_path
        self._out_format  = out_format
        self._keep_aspect = keep_aspect

    def run(self):
        try:
            ok = self._handler.resize_document(
                self._width, self._height, self._unit,
                self._output_path, self._out_format, self._keep_aspect)
            self.finished.emit(ok, self._output_path)
        except Exception as e:
            self.error.emit(str(e))


