class ResizeDialog(QDialog):
    """
    Modal dialog for resizing the document.
    Remembers the last-used values across invocations (class-level).
    """
    _last_width:  float = 210.0
    _last_height: float = 297.0
    _last_unit:   str   = "mm"
    _last_format: str   = "pdf"
    _last_aspect: bool  = False

    UNITS   = ["mm", "cm", "inches", "pixels"]
    FORMATS = ["pdf", "png", "jpg"]

    def __init__(self, handler: PDFHandler, parent=None):
        super().__init__(parent)
        self._handler = handler
        self.setWindowTitle("Resize Document")
        self.setMinimumWidth(360)
        self.setModal(True)
        self._build_ui()
        self._connect()
        self._update_preview()

    # ── UI construction ──────────────────────────────────────────────
    def _build_ui(self):
        outer = QVBoxLayout(self)
        outer.setSpacing(14)
        outer.setContentsMargins(20, 20, 20, 16)

        # ── Dimensions group ─────────────────────────────────────────
        dim_group = QGroupBox("Target Dimensions")
        dim_form  = QFormLayout(dim_group)
        dim_form.setSpacing(8)

        self._spin_w = QDoubleSpinBox()
        self._spin_w.setRange(0.1, 99999)
        self._spin_w.setDecimals(2)
        self._spin_w.setValue(ResizeDialog._last_width)
        self._spin_w.setSingleStep(1.0)

        self._spin_h = QDoubleSpinBox()
        self._spin_h.setRange(0.1, 99999)
        self._spin_h.setDecimals(2)
        self._spin_h.setValue(ResizeDialog._last_height)
        self._spin_h.setSingleStep(1.0)

        self._combo_unit = QComboBox()
        self._combo_unit.addItems(self.UNITS)
        idx = self.UNITS.index(ResizeDialog._last_unit) \
              if ResizeDialog._last_unit in self.UNITS else 0
        self._combo_unit.setCurrentIndex(idx)

        self._chk_aspect = QCheckBox("Maintain aspect ratio (adjusts height)")
        self._chk_aspect.setChecked(ResizeDialog._last_aspect)

        dim_form.addRow("Width:", self._spin_w)
        dim_form.addRow("Height:", self._spin_h)
        dim_form.addRow("Unit:", self._combo_unit)
        dim_form.addRow("", self._chk_aspect)
        outer.addWidget(dim_group)

        # ── Output format ────────────────────────────────────────────
        fmt_group = QGroupBox("Output Format")
        fmt_form  = QFormLayout(fmt_group)
        self._combo_format = QComboBox()
        self._combo_format.addItems(self.FORMATS)
        idx = self.FORMATS.index(ResizeDialog._last_format) \
              if ResizeDialog._last_format in self.FORMATS else 0
        self._combo_format.setCurrentIndex(idx)
        fmt_form.addRow("Save as:", self._combo_format)
        outer.addWidget(fmt_group)

        # ── Preview label ────────────────────────────────────────────
        self._lbl_preview = QLabel()
        self._lbl_preview.setObjectName("info_label")
        self._lbl_preview.setWordWrap(True)
        self._lbl_preview.setStyleSheet("color:#68d391; font-size:11px;")
        outer.addWidget(self._lbl_preview)

        # ── Dialog buttons ───────────────────────────────────────────
        self._btn_box = QDialogButtonBox(
            QDialogButtonBox.StandardButton.Ok |
            QDialogButtonBox.StandardButton.Cancel)
        self._btn_box.button(
            QDialogButtonBox.StandardButton.Ok).setText("Resize & Save")
        outer.addWidget(self._btn_box)

    def _connect(self):
        self._spin_w.valueChanged.connect(self._update_preview)
        self._spin_h.valueChanged.connect(self._update_preview)
        self._combo_unit.currentTextChanged.connect(self._update_preview)
        self._chk_aspect.toggled.connect(self._on_aspect_toggled)
        self._btn_box.accepted.connect(self._on_accept)
        self._btn_box.rejected.connect(self.reject)

    # ── Slots ────────────────────────────────────────────────────────
    def _on_aspect_toggled(self, checked: bool):
        self._spin_h.setEnabled(not checked)
        self._update_preview()

    def _update_preview(self):
        """Show computed dimensions in points and mm."""
        w = self._spin_w.value()
        h = self._spin_h.value()
        unit = self._combo_unit.currentText()
        aspect = self._chk_aspect.isChecked()
        w_pt, h_pt = self._handler.get_resized_preview_size(w, h, unit, aspect)
        if w_pt > 0 and h_pt > 0:
            w_mm = w_pt / 72.0 * 25.4
            h_mm = h_pt / 72.0 * 25.4
            self._lbl_preview.setText(
                f"Output size: {w_pt:.0f} × {h_pt:.0f} pt  "
                f"({w_mm:.1f} × {h_mm:.1f} mm)")
        else:
            self._lbl_preview.setText("⚠  Invalid dimensions")

    def _on_accept(self):
        """Persist last-used values then accept."""
        ResizeDialog._last_width  = self._spin_w.value()
        ResizeDialog._last_height = self._spin_h.value()
        ResizeDialog._last_unit   = self._combo_unit.currentText()
        ResizeDialog._last_format = self._combo_format.currentText()
        ResizeDialog._last_aspect = self._chk_aspect.isChecked()
        self.accept()

    # ── Public accessors ─────────────────────────────────────────────
    @property
    def width(self) -> float:  return self._spin_w.value()
    @property
    def height(self) -> float: return self._spin_h.value()
    @property
    def unit(self) -> str:     return self._combo_unit.currentText()
    @property
    def out_format(self) -> str: return self._combo_format.currentText()
    @property
    def keep_aspect(self) -> bool: return self._chk_aspect.isChecked()


