    # ------------------------------------------------------------------ #
    #  Signal wiring
    # ------------------------------------------------------------------ #

    def _connect_signals(self):
        self._btn_open.clicked.connect(self._open_file)
        self._btn_close.clicked.connect(self._close_file)
        self._btn_prev.clicked.connect(self._prev_page)
        self._btn_next.clicked.connect(self._next_page)

        self._zoom_slider.valueChanged.connect(self._on_zoom_slider)
        self._btn_zoom_in.clicked.connect(self._zoom_in)
        self._btn_zoom_out.clicked.connect(self._zoom_out)

        self._canvas.selection_changed.connect(self._on_selection_changed)
        self._canvas.selection_cleared.connect(self._on_selection_cleared)
        self._canvas.zoom_changed.connect(self._on_canvas_zoom_changed)

        self._btn_add_to_queue.clicked.connect(self._add_to_queue)
        self._btn_clear_sel.clicked.connect(self._canvas.clear_selection)
        self._btn_remove.clicked.connect(self._remove_from_queue)
        self._btn_clear_queue.clicked.connect(self._clear_queue)
        self._list_queue.itemSelectionChanged.connect(self._on_queue_selection)

        self._btn_export_pdf.clicked.connect(self._export_pdf)
        self._btn_export_img.clicked.connect(self._export_image)
        self._btn_resize.clicked.connect(self._open_resize_dialog)

        QShortcut(QKeySequence("Ctrl++"), self).activated.connect(self._zoom_in)
        QShortcut(QKeySequence("Ctrl+="), self).activated.connect(self._zoom_in)
        QShortcut(QKeySequence("Ctrl+-"), self).activated.connect(self._zoom_out)
        QShortcut(QKeySequence("Left"),   self).activated.connect(self._prev_page)
        QShortcut(QKeySequence("Right"),  self).activated.connect(self._next_page)
        QShortcut(QKeySequence("Ctrl+O"), self).activated.connect(self._open_file)
        QShortcut(QKeySequence("Ctrl+W"), self).activated.connect(self._close_file)

    # ------------------------------------------------------------------ #
    #  File loading (threaded)
    # ------------------------------------------------------------------ #

    def _open_file(self):
        """Open PDF or image file via dialog."""
        path, _ = QFileDialog.getOpenFileName(
            self, "Open File", "", self._OPEN_FILTER)
        if not path:
            return
        self._load_file(path)

    def _load_file(self, path: str):
        """Common loader — used by dialog and CLI argument."""
        self._progress.setVisible(True)
        self._progress.setRange(0, 0)
        self._status_label.setText(f"Loading {Path(path).name}…")
        self._btn_open.setEnabled(False)

        self._load_thread = QThread()
        self._load_worker = LoadWorker(self._handler, path)
        self._load_worker.moveToThread(self._load_thread)
        self._load_thread.started.connect(self._load_worker.run)
        self._load_worker.finished.connect(self._on_loaded)
        self._load_worker.error.connect(self._on_load_error)
        self._load_worker.finished.connect(self._load_thread.quit)
        self._load_worker.error.connect(self._load_thread.quit)
        self._load_thread.start()

    def _on_loaded(self, page_count: int):
        self._page_count   = page_count
        self._current_page = 0
        self._crop_queue.clear()
        self._list_queue.clear()

        self._lbl_filename.setText(self._handler.file_name)
        self._lbl_filename.setStyleSheet("color:#e2e8f0; font-size:12px;")
        self._canvas.set_view_zoom(self._zoom)
        self._canvas.load_page(0)

        self._update_controls()
        self._btn_open.setEnabled(True)
        self._progress.setVisible(False)

        ftype = self._handler.file_type.upper()
        self._status_label.setText(
            f"Loaded {ftype}  '{self._handler.file_name}'  —  "
            f"{page_count} page(s)")

    def _on_load_error(self, msg: str):
        self._btn_open.setEnabled(True)
        self._progress.setVisible(False)
        QMessageBox.critical(self, "Failed to open file",
                             f"Could not load the file:\n\n{msg}")
        self._status_label.setText("Error loading file.")

    # ── CLI helper ────────────────────────────────────────────────────
    def _open_file_directly(self, path: str):
        """Open a file without showing the dialog (e.g. from CLI)."""
        if not Path(path).is_file():
            return
        self._load_file(path)

    # ------------------------------------------------------------------ #
    #  Page navigation
    # ------------------------------------------------------------------ #

    def _prev_page(self):
        if self._current_page > 0:
            self._current_page -= 1
            self._canvas.load_page(self._current_page)
            self._update_controls()

    def _next_page(self):
        if self._current_page < self._page_count - 1:
            self._current_page += 1
            self._canvas.load_page(self._current_page)
            self._update_controls()

    # ------------------------------------------------------------------ #
    #  Zoom
    # ------------------------------------------------------------------ #

    def _on_zoom_slider(self, value: int):
        self._zoom = value / 100.0
        self._lbl_zoom.setText(f"{value}%")
        self._canvas.set_view_zoom(self._zoom)

    def _zoom_in(self):
        new_val = min(self._zoom_slider.value() + 15, 400)
        self._zoom_slider.setValue(new_val)

    def _zoom_out(self):
        new_val = max(self._zoom_slider.value() - 15, 30)
        self._zoom_slider.setValue(new_val)

    def _on_canvas_zoom_changed(self, percent: int):
        self._zoom_slider.blockSignals(True)
        clamped = max(self._zoom_slider.minimum(),
                      min(percent, self._zoom_slider.maximum()))
        self._zoom_slider.setValue(clamped)
        self._lbl_zoom.setText(f"{clamped}%")
        self._zoom = clamped / 100.0
        self._zoom_slider.blockSignals(False)

    # ------------------------------------------------------------------ #
    #  Selection handling
    # ------------------------------------------------------------------ #

    def _on_selection_changed(self, rect: QRectF):
        w = rect.width(); h = rect.height()
        # Show dimensions in pt and mm for PDFs; pixels for images
        if self._handler.file_type == "image":
            self._lbl_sel_info.setText(
                f"Selected  {w:.0f} × {h:.0f} px")
        else:
            self._lbl_sel_info.setText(
                f"Selected  {w:.1f} × {h:.1f} pt\n"
                f"({w/72*25.4:.1f} × {h/72*25.4:.1f} mm)")
        self._lbl_sel_info.setStyleSheet("color:#68d391; font-size:11px;")
        self._btn_add_to_queue.setEnabled(True)
        self._btn_clear_sel.setEnabled(True)
        self._btn_export_img.setEnabled(True)
        self._status_label.setText(
            f"Selection: {w:.0f}×{h:.0f} on page {self._current_page+1}")

    def _on_selection_cleared(self):
        self._lbl_sel_info.setText("Draw a rectangle on the page")
        self._lbl_sel_info.setStyleSheet("")
        self._btn_add_to_queue.setEnabled(False)
        self._btn_clear_sel.setEnabled(False)
        if not self._crop_queue:
            self._btn_export_img.setEnabled(False)

    # ------------------------------------------------------------------ #
    #  Crop queue
    # ------------------------------------------------------------------ #

    def _add_to_queue(self):
        region = self._canvas.make_crop_region()
        if region is None:
            return
        self._crop_queue.append(region)
        label = (f"Page {region.page_index+1}  "
                 f"({region.rect.width():.0f}×{region.rect.height():.0f})")
        item = QListWidgetItem(f"✂  {label}")
        item.setToolTip(
            f"x={region.rect.x():.1f}, y={region.rect.y():.1f}, "
            f"w={region.rect.width():.1f}, h={region.rect.height():.1f}"
        )
        self._list_queue.addItem(item)
        self._update_queue_controls()
        self._canvas.clear_selection()
        self._status_label.setText(
            f"Added crop #{len(self._crop_queue)} to the queue")

    def _remove_from_queue(self):
        row = self._list_queue.currentRow()
        if 0 <= row < len(self._crop_queue):
            self._crop_queue.pop(row)
            self._list_queue.takeItem(row)
            self._update_queue_controls()

    def _clear_queue(self):
        self._crop_queue.clear()
        self._list_queue.clear()
        self._update_queue_controls()
        self._status_label.setText("Queue cleared")

    def _on_queue_selection(self):
        has_sel = bool(self._list_queue.selectedItems())
        self._btn_remove.setEnabled(has_sel)

    def _update_queue_controls(self):
        has_items = bool(self._crop_queue)
        self._btn_export_pdf.setEnabled(has_items)
        self._btn_clear_queue.setEnabled(has_items)

    # ------------------------------------------------------------------ #
    #  Export
    # ------------------------------------------------------------------ #

    def _export_pdf(self):
        if not self._crop_queue:
            QMessageBox.information(self, "Nothing to export",
                                    "Add at least one crop region to the queue first.")
            return
        stem = Path(self._handler.file_name).stem
        default = f"{stem}_cropped.pdf"
        out_path, _ = QFileDialog.getSaveFileName(
            self, "Export as PDF", default, "PDF Files (*.pdf)")
        if not out_path:
            return

        self._progress.setVisible(True)
        self._progress.setRange(0, 0)
        self._status_label.setText("Exporting PDF…")
        self._btn_export_pdf.setEnabled(False)

        regions_snapshot = list(self._crop_queue)
        self._export_thread = QThread()
        self._export_worker = ExportWorker(
            self._handler, regions_snapshot, out_path)
        self._export_worker.moveToThread(self._export_thread)
        self._export_thread.started.connect(self._export_worker.run)
        self._export_worker.finished.connect(self._on_export_done)
        self._export_worker.error.connect(self._on_export_error)
        self._export_worker.finished.connect(self._export_thread.quit)
        self._export_worker.error.connect(self._export_thread.quit)
        self._export_thread.finished.connect(self._export_thread.deleteLater)
        self._export_thread.start()

    def _on_export_done(self, ok: bool, out_path: str, crop_count: int):
        self._progress.setVisible(False)
        self._btn_export_pdf.setEnabled(bool(self._crop_queue))
        if ok:
            self._status_label.setText(f"Saved  →  {Path(out_path).name}")
            QMessageBox.information(
                self, "Export complete",
                f"Saved {crop_count} page(s) to:\n{out_path}")
        else:
            self._status_label.setText("Export failed.")
            QMessageBox.critical(self, "Export failed",
                                 "Could not write the output PDF.")

    def _on_export_error(self, msg: str):
        self._progress.setVisible(False)
        self._btn_export_pdf.setEnabled(bool(self._crop_queue))
        self._status_label.setText("Export error.")
        QMessageBox.critical(self, "Export error",
                             f"An error occurred during export:\n{msg}")

    def _export_image(self):
        region = self._canvas.make_crop_region()
        if region is None:
            if self._crop_queue:
                region = self._crop_queue[-1]
            else:
                QMessageBox.information(self, "No selection",
                                        "Select a region on the page first.")
                return
        stem = Path(self._handler.file_name).stem
        default = f"{stem}_crop.png"
        path, _ = QFileDialog.getSaveFileName(
            self, "Export as Image", default,
            "PNG Image (*.png);;JPEG Image (*.jpg *.jpeg)")
        if not path:
            return

        ok = self._handler.export_region_as_image(region, path, dpi=150)
        if ok:
            self._status_label.setText(f"Image saved  →  {Path(path).name}")
            QMessageBox.information(self, "Image exported",
                                    f"Saved to:\n{path}")
        else:
            QMessageBox.critical(self, "Export failed",
                                 "Could not save the image.")

    # ------------------------------------------------------------------ #
    #  Resize (Feature 2)
    # ------------------------------------------------------------------ #

    def _open_resize_dialog(self):
        if not self._handler.is_open:
            return
        dlg = ResizeDialog(self._handler, parent=self)
        dlg.setStyleSheet(self.styleSheet())
        if dlg.exec() != QDialog.DialogCode.Accepted:
            return

        # Determine output file filter based on chosen format
        fmt = dlg.out_format
        stem = Path(self._handler.file_name).stem
        if fmt == "pdf":
            filter_str = "PDF Files (*.pdf)"
            default = f"{stem}_resized.pdf"
        elif fmt == "png":
            filter_str = "PNG Image (*.png)"
            default = f"{stem}_resized.png"
        else:
            filter_str = "JPEG Image (*.jpg *.jpeg)"
            default = f"{stem}_resized.jpg"

        out_path, _ = QFileDialog.getSaveFileName(
            self, "Save Resized File", default, filter_str)
        if not out_path:
            return

        self._progress.setVisible(True)
        self._progress.setRange(0, 0)
        self._status_label.setText("Resizing…")
        self._btn_resize.setEnabled(False)

        self._resize_thread = QThread()
        self._resize_worker = ResizeWorker(
            self._handler,
            dlg.width, dlg.height, dlg.unit,
            out_path, dlg.out_format, dlg.keep_aspect)
        self._resize_worker.moveToThread(self._resize_thread)
        self._resize_thread.started.connect(self._resize_worker.run)
        self._resize_worker.finished.connect(self._on_resize_done)
        self._resize_worker.error.connect(self._on_resize_error)
        self._resize_worker.finished.connect(self._resize_thread.quit)
        self._resize_worker.error.connect(self._resize_thread.quit)
        self._resize_thread.finished.connect(self._resize_thread.deleteLater)
        self._resize_thread.start()

    def _on_resize_done(self, ok: bool, out_path: str):
        self._progress.setVisible(False)
        self._btn_resize.setEnabled(self._handler.is_open)
        if ok:
            self._status_label.setText(f"Resized  →  {Path(out_path).name}")
            QMessageBox.information(
                self, "Resize complete",
                f"Saved resized file to:\n{out_path}")
        else:
            self._status_label.setText("Resize failed.")
            QMessageBox.critical(self, "Resize failed",
                                 "Could not resize the document.")

    def _on_resize_error(self, msg: str):
        self._progress.setVisible(False)
        self._btn_resize.setEnabled(self._handler.is_open)
        self._status_label.setText("Resize error.")
        QMessageBox.critical(self, "Resize error",
                             f"An error occurred during resize:\n{msg}")

    # ------------------------------------------------------------------ #
    #  Controls state
    # ------------------------------------------------------------------ #

    def _update_controls(self):
        has_doc = self._handler.is_open
        self._btn_prev.setEnabled(has_doc and self._current_page > 0)
        self._btn_next.setEnabled(
            has_doc and self._current_page < self._page_count - 1)
        self._btn_close.setEnabled(has_doc)
        self._btn_resize.setEnabled(has_doc)
        if has_doc:
            self._lbl_page.setText(
                f"{self._current_page+1} / {self._page_count}")
        else:
            self._lbl_page.setText("— / —")

    def _close_file(self):
        """Close the loaded file and reset the UI to its empty state."""
        if not self._handler.is_open:
            return

        self._handler.close()
        self._page_count   = 0
        self._current_page = 0

        self._crop_queue.clear()
        self._list_queue.clear()

        self._canvas.load_page(0)
        self._canvas.clear_selection()

        self._lbl_filename.setText("No file open")
        self._lbl_filename.setStyleSheet("color:#718096; font-size:12px;")

        self._lbl_sel_info.setText("Draw a rectangle on the page")
        self._lbl_sel_info.setStyleSheet("")
        self._btn_add_to_queue.setEnabled(False)
        self._btn_clear_sel.setEnabled(False)
        self._btn_export_pdf.setEnabled(False)
        self._btn_export_img.setEnabled(False)
        self._btn_resize.setEnabled(False)

        self._update_controls()
        self._status_label.setText("File closed — open a new file to continue")

    # Keep old name as alias for CLI compatibility
    def _close_pdf(self):
        self._close_file()

    def closeEvent(self, event):
        self._handler.close()
        super().closeEvent(event)
